    FirebaseApp.configure()

    return true
  }
}

@main
struct ThreadTutorialApp: App {
    
    @UIApplicationDelegateAdaptor(AppDelegate.self) var delegate
    
    var body: some Scene {
        WindowGroup {
            ContentView()
        }
    }
}
